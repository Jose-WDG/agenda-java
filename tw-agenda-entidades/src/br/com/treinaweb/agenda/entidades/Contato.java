package br.com.treinaweb.agenda.entidades;

public class Contato {

	private int id;
	private String nome;
	private int idade;
	private String tell;
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public int getIdade() {
		return idade;
	}

	public void setIdade(int idade) {
		this.idade = idade;
	}

	public String getTell() {
		return tell;
	}

	public void setTell(String telefone) {
		this.tell = telefone;
	}

}
